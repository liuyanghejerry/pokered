fn glyph_for(ch: char) -> &'static [u8; GLYPH_SIZE] {
    let code = ch as u32;
    if code >= 0x20 && code <= 0x7E {
        &FONT_DATA[(code - 0x20) as usize]
    } else {
        &FONT_DATA[0] // fallback to space
    }
}

pub fn draw_char(ch: char, x: u32, y: u32, color: Rgba, fb: &mut FrameBuffer) {
    let glyph = glyph_for(ch);
    for row in 0..GLYPH_SIZE as u32 {
        let py = y + row;
        if py >= SCREEN_HEIGHT {
            break;
        }
        let byte = glyph[row as usize];
        for col in 0..GLYPH_SIZE as u32 {
            let px = x + col;
            if px >= SCREEN_WIDTH {
                break;
            }
            if byte & (0x80 >> col) != 0 {
                fb.set_pixel(px, py, color);
            }
        }
    }
}

pub fn draw_text(text: &str, x: u32, y: u32, color: Rgba, fb: &mut FrameBuffer) {
    let char_width = GLYPH_SIZE as u32;
    for (i, ch) in text.chars().enumerate() {
        let char_x = x + (i as u32 * char_width);
        if char_x + char_width > SCREEN_WIDTH {
            break;
        }
        draw_char(ch, char_x, y, color, fb);
    }
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn glyph_count_matches() {
        assert_eq!(FONT_DATA.len(), GLYPH_COUNT);
        assert_eq!(GLYPH_COUNT, 95);
    }

    #[test]
    fn space_is_blank() {
        let glyph = glyph_for(' ');
        assert!(glyph.iter().all(|&b| b == 0));
    }

    #[test]
    fn letter_a_has_pixels() {
        let glyph = glyph_for('A');
        assert!(glyph.iter().any(|&b| b != 0));
    }

    #[test]
    fn non_ascii_falls_back_to_space() {
        let glyph = glyph_for('日');
        assert!(glyph.iter().all(|&b| b == 0));
    }

    #[test]
    fn draw_text_does_not_panic() {
        let mut fb = FrameBuffer::new(Rgba::WHITE);
        draw_text("Hello, World!", 0, 0, Rgba::BLACK, &mut fb);
        draw_text("Edge", 150, 140, Rgba::BLACK, &mut fb);
        draw_text("", 0, 0, Rgba::BLACK, &mut fb);
    }

    #[test]
    fn draw_text_sets_pixels() {
        let mut fb = FrameBuffer::new(Rgba::WHITE);
        draw_text("A", 0, 0, Rgba::BLACK, &mut fb);
        let has_black = (0..8).any(|y| (0..8).any(|x| fb.get_pixel(x, y) == Some(Rgba::BLACK)));
        assert!(has_black, "Drawing 'A' should set at least one black pixel");
    }
}
